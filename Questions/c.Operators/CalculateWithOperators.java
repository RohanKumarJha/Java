class CalculateWithOperators {
    public static void main (String[] args) {
      int res = 1 + 2;
      System.out.println (res);
    //   res= res - 1;
      res -= 1;
      System.out.println (res);
    //   res = res * 2;
      res *= 2;
      System.out.println(res);
    //   res = res / 2;
      res /= 2;
      System.out.println(res);
    //   res = res + 8;
      res += 8;
    //   res = res % 7;
      res %= 7;
      System.out.println(res);
    }
  }
  